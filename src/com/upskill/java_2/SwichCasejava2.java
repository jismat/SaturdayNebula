package com.upskill.java_2;

public class SwichCasejava2 {

		public static void main(String[] args) {
			int day = 7;
			
			switch (day) {
			case 1:
				System.out.println("Monday");
				break;
			case 2:
				System.out.println("Tuesday");
				break;		
			case 3:
				System.out.println("Wednesday");
				break;		
			}
}}
